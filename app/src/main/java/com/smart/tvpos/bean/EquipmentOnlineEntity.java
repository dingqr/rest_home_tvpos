package com.smart.tvpos.bean;

public class EquipmentOnlineEntity {

    private int type;
    private int numAll;
    private int numOnline;
    private int numOffline;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getNumAll() {
        return numAll;
    }

    public void setNumAll(int numAll) {
        this.numAll = numAll;
    }

    public int getNumOnline() {
        return numOnline;
    }

    public void setNumOnline(int numOnline) {
        this.numOnline = numOnline;
    }

    public int getNumOffline() {
        return numOffline;
    }

    public void setNumOffline(int numOffline) {
        this.numOffline = numOffline;
    }
}
